// Packed bitmaps indexed by ASCII (0..127); each row is 5 low bits.
static uint8_t FONT_BITS[128][FONT_H];
static uint8_t FONT_HAS[128];

static inline void font_init(void)
{
	memset(FONT_BITS, 0, sizeof(FONT_BITS));
	memset(FONT_HAS, 0, sizeof(FONT_HAS));
	for (size_t i = 0; i < sizeof(FONT_ART) / sizeof(FONT_ART[0]); ++i)
	{
		unsigned char c = (unsigned char)FONT_ART[i].c;
		FONT_HAS[c] = 1;
		for (int r = 0; r < FONT_H; ++r)
		{
			uint8_t bits = 0;
			const char *row = FONT_ART[i].rows[r];
			for (int x = 0; x < FONT_W; ++x)
				if (row[x] != ' ' && row[x] != '\0')
					bits |= (uint8_t)(1u << (FONT_W - 1 - x));
			FONT_BITS[c][r] = bits;
		}
	}
}

// Returns the 7-row bitmap for a character (uppercased), or NULL if blank.
static inline const uint8_t *font_glyph(char ch)
{
	unsigned char c = (unsigned char)ch;
	if (c >= 'a' && c <= 'z') c = (unsigned char)(c - 'a' + 'A');
	if (c < 128 && FONT_HAS[c]) return FONT_BITS[c];
	return NULL;
}

